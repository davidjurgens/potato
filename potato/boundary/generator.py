_LLM_PROMPT = """You are helping map a human annotator's decision boundary in a text labeling task.

The label set is: {labels}
The annotator just labeled the text below as "{label}".

TEXT:
\"\"\"{text}\"\"\"

Generate exactly {n_flip} FLIP probe(s) and {n_invariance} INVARIANCE probe(s):
- FLIP: the SMALLEST possible edit that would plausibly change the correct label away from "{label}". Change as few words as possible; keep everything else character-identical.
- INVARIANCE: a paraphrase that rewords the text but must NOT change the correct label.

Respond with ONLY a JSON object of this shape (no prose, no markdown fences):
{{"probes": [{{"text": "...", "kind": "flip", "edit_hint": "<= 8 words describing the edit"}}]}}
"""


class ProbeGenerator:
    """Generates counterfactual probes via precomputed -> LLM -> rules tiers."""

    def __init__(self, app_config: Dict[str, Any], boundary_config) -> None:
        self.app_config = app_config
        self.boundary_config = boundary_config
        self._endpoint = None
        self._endpoint_failed = False

    # -- LLM endpoint (lazy, mirrors JudgeService) --------------------------
    def _get_endpoint(self):
        if self._endpoint is not None or self._endpoint_failed:
            return self._endpoint
        ai_support = self.boundary_config.ai_support or self.app_config.get("ai_support")
        if not ai_support or not ai_support.get("enabled", False):
            self._endpoint_failed = True
            return None
        try:
            from potato.ai.ai_endpoint import AIEndpointFactory

            self._endpoint = AIEndpointFactory.create_endpoint({"ai_support": ai_support})
        except Exception:
            logger.exception("Boundary Lab: failed to create AI endpoint")
            self._endpoint_failed = True
        return self._endpoint

    # -- Tiers ---------------------------------------------------------------
    def _from_precomputed(self, item_data: Dict[str, Any]) -> List[Tuple[str, str, str]]:
        entries = item_data.get(self.boundary_config.precomputed_key)
        if not isinstance(entries, list):
            return []
        results = []
        for entry in entries:
            if not isinstance(entry, dict):
                continue
            text = entry.get("text")
            kind = entry.get("kind", KIND_FLIP)
            if not text or kind not in VALID_KINDS:
                continue
            results.append((text, kind, entry.get("edit_hint", "")))
        return results

    def _from_llm(self, text: str, label: str, labels: List[str],
                  n_flip: int, n_invariance: int) -> List[Tuple[str, str, str]]:
        endpoint = self._get_endpoint()
        if endpoint is None or (n_flip <= 0 and n_invariance <= 0):
            return []
        prompt = _LLM_PROMPT.format(
            labels=", ".join(labels), label=label, text=text,
            n_flip=max(n_flip, 0), n_invariance=max(n_invariance, 0),
        )
        try:
            # Counterfactual JSON for longer texts needs headroom (gemma-style
            # models fence output and truncate at small max_tokens).
            original_max = getattr(endpoint, "max_tokens", None)
            if original_max is not None and original_max < 1024:
                endpoint.max_tokens = 1024
            try:
                response = endpoint.query(prompt, ProbeSet)
            finally:
                if original_max is not None:
                    endpoint.max_tokens = original_max
            if isinstance(response, ProbeSet):
                specs = [p.model_dump() for p in response.probes]
            else:
                parsed = endpoint.parseStringToJson(response)
                if isinstance(parsed, dict):
                    specs = parsed.get("probes", [])
                elif isinstance(parsed, list):
                    specs = parsed
                else:
                    specs = []
            results = []
            for spec in specs:
                if not isinstance(spec, dict):
                    continue
                probe_text = spec.get("text")
                kind = spec.get("kind", KIND_FLIP)
                if not probe_text or probe_text == text or kind not in VALID_KINDS:
                    continue
                results.append((probe_text, kind, spec.get("edit_hint", "")))
            return results
        except Exception:
            logger.exception("Boundary Lab: LLM probe generation failed; falling back")
            return []

    # -- Public API ------------------------------------------------------------
    def generate(self, instance_id: str, schema: str, label: str,
                 labels: List[str], text: str,
                 item_data: Optional[Dict[str, Any]] = None) -> List[Probe]:
        """Generate up to ``probes_per_item`` probes for (instance, schema, label)."""
        bc = self.boundary_config
        budget = bc.probes_per_item
        n_invariance = 1 if (bc.include_invariance and budget > 1) else 0
        n_flip = budget - n_invariance

        # Each candidate is (text, kind, edit_hint, source).
        candidates: List[Tuple[str, str, str, str]] = []

        def count(kind: str) -> int:
            return sum(1 for c in candidates if c[1] == kind)

        for source in bc.sources:
            remaining_flip = n_flip - count(KIND_FLIP)
            remaining_inv = n_invariance - count(KIND_INVARIANCE)
            if remaining_flip <= 0 and remaining_inv <= 0:
                break
            if source == "precomputed":
                tier = self._from_precomputed(item_data or {})
            elif source == "llm":
                tier = self._from_llm(text, label, labels, remaining_flip, remaining_inv)
            else:
                tier = generate_rule_probes(text, remaining_flip, remaining_inv)
            for probe_text, kind, hint in tier:
                if not isinstance(probe_text, str) or not probe_text.strip():
                    continue
                if kind == KIND_FLIP and count(KIND_FLIP) >= n_flip:
                    continue
                if kind == KIND_INVARIANCE and count(KIND_INVARIANCE) >= n_invariance:
                    continue
                if probe_text == text or any(c[0] == probe_text for c in candidates):
                    continue
                candidates.append((probe_text, kind, hint, source))

        # Show flips first, invariance last (the "gotcha" reads better at the end).
        ordered = [c for c in candidates if c[1] == KIND_FLIP] + \
                  [c for c in candidates if c[1] == KIND_INVARIANCE]
        return [
            Probe(
                probe_id=make_probe_id(instance_id, schema, label, probe_text),
                instance_id=str(instance_id),
                schema=schema,
                original_label=label,
                original_text=text,
                text=probe_text,
                kind=kind,
                source=source,
                edit_hint=hint,
            )
            for probe_text, kind, hint, source in ordered
        ]
